/*************************************************************************
      Description of the set D of output differences reachable at the
      output of the SHA-1 compression function.

      Each structure contains a decription of the zi equations and a
      list of output differences with the corresponding probabilities
      (for steps 61 to 79, stored as -log2(p)).

      The zi are coded as: z1 z7 z2 z3 z8 z6 z4 z5 (z1 is the MSB)
      The effect of z9...z13 is not considered in this file, because it
      just adds a constant value to the differences:
      z9  adds {(1<<6), (1<<1), 0, 0, 0}
      z10 adds {(1<<9), (1<<4), 0, 0, 0},
      z11 adds {(1<<13), (1<<8), 0, 0, 0},
      z12 adds {(1<<3), 0, 0, 0, 0},
      z13 adds {(1<<5), 0, 0, 0, 0},

      Note that some combinaisons of z_i's are missing because the
      corresponding set of output differences is identical to (or smaller
      than) the set obtained with another combinaison.

      WARNING: the zi's must actually be flipped !!
      (they are written for m' rather than m)
 *************************************************************************/


